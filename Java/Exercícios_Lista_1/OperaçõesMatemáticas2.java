public class OperaçõesMatemáticas {
    
    public static void main(String[] args)
    {
        
        int numint = 20;
        float numfloat = 33.9f;
        double numdouble = 44.7325;
        
        int soma_int = numint + numint;
        float multiplicação_float_int = numfloat + numint;
        double subtração_int_double = numint - numdouble;
        double divisão_double_float = numdouble / numfloat;
        
        System.out.println(soma_int);
        System.out.println(multiplicação_float_int);
        System.out.println(subtração_int_double);
        System.out.println(divisão_double_float);
        
    }
    
}
