package Construtor2;

public class Cachorro {

	private String nome;
	private String cor;
	
	Cachorro(String nome, String cor)
	{
		
		this.nome = nome;
		this.cor = cor;
		
	}
	
	public String getNome()
	{
		
		return nome;
		
	}
	
	public String getCor()
	{
		
		return cor;
		
	}
	
}
