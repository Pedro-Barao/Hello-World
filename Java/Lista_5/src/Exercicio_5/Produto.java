package Exercicio_5;

public class Produto {
	
	private String nome;
	private double preco;
	
	Produto(String nome, double preco)
	{
		
		this.nome = nome;
		this.preco = preco;
		
	}
	
	public String getNome()
	{
		
		return nome;
		
	}
	
	public double getPreco()
	{
		
		return preco;
		
	}
	
}
