package Exercicio_4;

public class Item {

	private String nome;
	private String categoria;
	
	Item(String nome, String categoria)
	{
		
		this.nome = nome;
		this.categoria = categoria;
		
	}
	
	public String getNome()
	{
		
		return nome;
		
	}
	
	public String getCategoria()
	{
		
		return categoria;
		
	}
	
}
