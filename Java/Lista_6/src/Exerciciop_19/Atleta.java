package Exerciciop_19;

public class Atleta {

	private String nome;
	private int pontuacao;
	
	Atleta(String nome, int pontuacao)
	{
		
		this.nome = nome;
		this.pontuacao = pontuacao;
		
	}
	
	public String getNome()
	{
		
		return nome;
		
	}
	
	public int getPontuacao()
	{
		
		return pontuacao;
		
	}
	
}
