package Exercicio_1;

public class Livro {
	
	private String titulo;
	private String autor;
	
	Livro(String titulo, String autor)
	{
		
		this.titulo = titulo;
		this.autor = autor;
		
	}
	
	public String getTitulo()
	{
		
		return titulo;
		
	}
	
	public String getAutor()
	{
		
		return autor;
		
	}

}
