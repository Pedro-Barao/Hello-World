package Exercicio_8;

public class Usuario {

	private int id;
	private String nome;
	
	Usuario(int id, String nome)
	{
		
		this.id = id;
		this.nome = nome;
		
	}
	
	public int getId()
	{
		
		return id;
		
	}
	
	public String getNome()
	{
		
		return nome;
		
	}
	
}
