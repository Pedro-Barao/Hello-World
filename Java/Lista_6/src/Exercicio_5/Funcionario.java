package Exercicio_5;

public class Funcionario {

	private String nome;
	private String departamento;
	
	Funcionario(String nome, String departamento)
	{
		
		this.nome = nome;
		this.departamento = departamento;
		
	}
	
	public String getNome()
	{
		
		return nome;
		
	}
	
	public String getDepartamento()
	{
		
		return departamento;
		
	}
	
}
