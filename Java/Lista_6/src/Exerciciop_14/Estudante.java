package Exerciciop_14;

public class Estudante {

	protected String nome;
	protected double nota;
	
	Estudante(String nome, double nota)
	{
		
		this.nome = nome;
		this.nota = nota;
		
	}
	
	public String getNome()
	{
		
		return nome;
		
	}
	
	public double getNota()
	{
		
		return nota;
		
	}
	
}
